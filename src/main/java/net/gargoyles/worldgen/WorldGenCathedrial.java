	@SuppressWarnings("deprecation")
	public boolean LocationIsValidSpawn(World world, int x, int y, int z)
	{

		IBlockState checkBlock = world.getBlockState(new BlockPos(x, y, z));
		IBlockState blockAbove = world.getBlockState(new BlockPos(x, y + 1, z));
		IBlockState blockBelow = world.getBlockState(new BlockPos(x, y - 1, z));

		if (blockAbove.getBlock() != Blocks.AIR)
		{
			return false;
		}
		if (checkBlock.getBlock().isOpaqueCube(checkBlock) && world.canSeeSky(new BlockPos(x, y + 1, z)))
		{
			return true;
		}
		else if (checkBlock.getBlock() == Blocks.SNOW_LAYER && blockBelow.getBlock().isOpaqueCube(blockBelow) && world.canSeeSky(new BlockPos(x, y + 1, z)))
		{
			return true;
		}
		else if (checkBlock.getBlock().getMaterial(checkBlock) == Material.PLANTS && blockBelow.getBlock().isOpaqueCube(blockBelow) && world.canSeeSky(new BlockPos(x, y + 1, z)))
		{
			return true;
		}
		else
		{
			return false;
		}
	}

